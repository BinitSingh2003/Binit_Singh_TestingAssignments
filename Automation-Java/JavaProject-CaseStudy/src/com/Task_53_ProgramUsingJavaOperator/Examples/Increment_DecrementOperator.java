package com.Task_53_ProgramUsingJavaOperator.Examples;

public class Increment_DecrementOperator {

	public static void main(String[] args) {
		int visitors = 250;

		visitors++;
		System.out.println("After New Visitor: " + visitors);

		visitors--;
		System.out.println("After One Visitor Left: " + visitors);

	}

}
